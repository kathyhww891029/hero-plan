// ── 作业（写作业）───────────────────────────────────────────────
//   - 完成作业（家长确认）：+2分
//   - 专注块：第1块+1分，第2块+2分，第3块+3分（递增奖励）
//   - 满分：2 + (1+2+3) = 8分
const HW_COMPLETE_SCORE = 2;          // 完成作业奖励
const HW_MAX_BLOCKS = 3;              // 最大专注块数
const HW_BLOCK_MINUTES = 10;          // 每块专注时长（分钟）
// 专注块积分：HW_BLOCK_SCORES[blockIndex] = 该块得分（1-indexed）
const HW_BLOCK_SCORES = [1, 2, 3];    // 第1块+1, 第2块+2, 第3块+3
// 计算 N 个专注块的积分
function calcHwBlockScore(blocks) {
  let score = 0;
  for (let i = 0; i < Math.min(blocks, HW_MAX_BLOCKS); i++) {
    score += HW_BLOCK_SCORES[i];
  }
  return score;
}

